export const products: Product[] = [...baseProducts, ...extraProducts];

export const popularProducts = products.filter((product) => product.isPopular || product.isHero).slice(0, 8);

export const getProduct = (id?: string) => products.find((product) => product.id === id);

export const getProductsByRoom = (roomId?: string) =>
  roomId ? products.filter((product) => product.room === roomId) : products;

export const getRelatedProducts = (product: Product) =>
  products
    .filter((item) => item.id !== product.id && (item.room === product.room || item.category === product.category))
    .slice(0, 3);
